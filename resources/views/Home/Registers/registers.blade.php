		$("input[name='phone']").blur(function(){
			// 获取手机号
			p=$(this).val();
			o=$(this);
			// 匹配正则 match 匹配不到的话返回null
			if(p.match(/^\d{11}$/)==null){
				// 把获取校验码的按钮 设置禁用
				$("#get_code").attr('disabled',true);
				$(this).next("span").css('color','red').html('手机号码不合法!');
			}else{
				$.get("/check_phone",{p:p},function(res){
					// alert(res);
					if(res==0){
						// 把获取校验码的按钮 设置可用
						$("#get_code").attr('disabled',false);
						o.next('span').css('color','green').html('手机号码可以使用.');
						PHONE=true;
					}else{
						// 把获取校验码的按钮 设置禁用
						$("#get_code").attr('disabled',true);
						o.next('span').css('color','red').html('手机号码已注册！');
					}
				});
			}
			
		});
		// 获取发送验证码按钮 绑定单击事件
